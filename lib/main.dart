      ),
      backgroundColor: Colors.indigo,
      body: const Center(
        child: Text(
          'Hello there!',
          style: TextStyle(
            fontSize: 24,
            color: Colors.white,
            fontWeight: FontWeight.bold,
          ),
        ),
      ),
    );
  }
}
